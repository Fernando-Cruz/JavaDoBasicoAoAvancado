package PrimeiroPassos;

public class OperadoresAritimeticos{
    public static void main(String[] args){
        double numero1 = 5;
        double numero2 = 5.5;

        System.out.println("Adição : "+ numero1 + " + " + numero2 + " = " + (numero1 + numero2));
        System.out.println("Subtação : "+ numero1 + " - " + numero2 + " = " + (numero1 - numero2));
        System.out.println("Multiplicação : "+ numero1 + " * " + numero2 + " = " + (numero1 * numero2));
        System.out.println("Divisão : "+ numero1 + " / " + numero2 + " = " + (numero1 / numero2));
        System.out.println("resto : "+ numero1 + " = " + (numero1 % 2));
    }
}
